package _34_instanceof;

public class A_InstanceOf {
    public static void main(String[] args) {
        Integer age = 31;
        String name = "Pham Quang Hien";
        Float salary = null;

        System.out.println(age instanceof Integer);
        System.out.println(name instanceof String);
        System.out.println(salary instanceof Float);
    }
}
