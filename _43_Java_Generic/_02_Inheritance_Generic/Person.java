package _43_Java_Generic._02_Inheritance_Generic;

public class Person<K, V> {
    private K key;
    private V value;

    public Person(K key, V value) {
        this.key = key;
        this.value = value;
    }

    public K getKey() {
        return key;
    }

    public V getValue() {
        return value;
    }

    public void setKey(K key) {
        this.key = key;
    }

    public void setValue(V value) {
        this.value = value;
    }
}
